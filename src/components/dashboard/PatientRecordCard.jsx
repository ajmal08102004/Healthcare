import React from 'react';
import { 
  User, 
  Calendar, 
  Phone, 
  Mail, 
  TrendingUp, 
  Clock, 
  Target, 
  AlertCircle,
  CheckCircle,
  Activity,
  Heart,
  Zap,
  Award,
  Flame,
  Download,
  FileText,
  Printer,
  Share2,
  Stethoscope,
  Pill,
  AlertTriangle
} from 'lucide-react';

